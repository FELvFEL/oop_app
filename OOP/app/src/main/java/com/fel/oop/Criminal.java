package com.fel.oop;

public class Criminal {
    String name;
    String year;
    String country;
    String crime;
    String signs;
    String uri;

    Criminal(String name, String year, String country, String crime, String signs, String uri){
        this.name=name;
        this.year=year;
        this.country=country;
        this.crime=crime;
        this.signs=signs;
        this.uri=uri;
    }
}
